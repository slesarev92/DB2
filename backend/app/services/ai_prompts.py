SENSITIVITY_EXPLAIN_TASK = """## ЗАДАЧА

Тебе передана матрица чувствительности (sensitivity analysis) NPV проекта \
к 4 параметрам (ND, offtake, shelf_price, COGS) на 5 уровнях отклонения \
(-20%, -10%, 0%, +10%, +20%). Базовый NPV Y1-Y10 и Contribution Margin \
ratio также даны.

Тебе нужно:

1. **Определить самый чувствительный параметр** — тот, у которого \
абсолютная дельта NPV при ±20% отклонении максимальна. Назови его и \
дай конкретную цифру: "при +20% ND → NPV +X млн ₽ (+Y%)".
2. **Определить наименее чувствительный параметр** — аналогично.
3. **Написать narrative** (3-4 предложения): интерпретация чувствительности \
для бизнеса. Что должен контролировать аналитик? Есть ли нелинейности \
(когда +10% и +20% дают непропорциональный эффект)?
4. **Actionable levers** — 2-3 конкретных рычага, которые аналитик \
может крутить: "увеличить ND в первые 12 месяцев", "пересмотреть \
COGS через альтернативного поставщика", и т.д.
5. **Warning flags** — red flags, если есть: гипер-чувствительность \
к одному параметру (>50% NPV swing при ±20%), asymmetric risk (downside \
значительно больше upside), CM ratio ниже 20% при любом delta.

## ФОРМАТ ОТВЕТА

ТОЛЬКО JSON, без markdown, без ``` fence:

{
  "most_sensitive_param": "название параметра",
  "most_sensitive_impact": "+20% ND → +X.X млн ₽ NPV (+Y%)",
  "least_sensitive_param": "название параметра",
  "narrative": "3-4 предложения интерпретации",
  "actionable_levers": ["рычаг 1", "рычаг 2"],
  "warning_flags": ["flag 1"]
}

`warning_flags` — пустой список если нет red flags. Не выдумывай flags \
только чтобы что-то написать."""


SENSITIVITY_EXPLAIN_SYSTEM = BASE_TONE_PROMPT + "\n\n" + SENSITIVITY_EXPLAIN_TASK
"""Полный system prompt для explain-sensitivity endpoint'а (Phase 7.3)."""


